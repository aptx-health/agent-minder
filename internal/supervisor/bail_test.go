func TestExtractBailReport_Valid(t *testing.T) {
	input := `I explored the codebase but this is too complex.

<bail-report>
{
  "reason": "Issue requires design decisions not specified",
  "files_examined": ["internal/auth/handler.go", "internal/auth/middleware.go", "pkg/token/jwt.go"],
  "plan": "1. Define the new auth flow\n2. Update middleware\n3. Add tests",
  "sub_issues": ["Refactor middleware to support multiple providers", "Add JWT validation tests"],
  "complexity": "large"
}
</bail-report>`

	report := extractBailReport(input)
	if report == nil {
		t.Fatal("expected bail report, got nil")
	}
	if report.Reason != "Issue requires design decisions not specified" {
		t.Errorf("reason = %q", report.Reason)
	}
	if len(report.FilesExamined) != 3 {
		t.Errorf("files_examined count = %d, want 3", len(report.FilesExamined))
	}
	if report.Complexity != "large" {
		t.Errorf("complexity = %q, want large", report.Complexity)
	}
	if len(report.SubIssues) != 2 {
		t.Errorf("sub_issues count = %d, want 2", len(report.SubIssues))
	}
}

