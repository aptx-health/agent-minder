func TestExtractBailReport_MultipleBlocks(t *testing.T) {
	// If the agent outputs multiple bail-report blocks (unlikely but possible),
	// we take the last one (LastIndex behavior).
	input := `<bail-report>
{"reason": "first attempt", "files_examined": [], "plan": "", "complexity": "small"}
</bail-report>

Actually let me try again...

<bail-report>
{"reason": "second attempt", "files_examined": ["main.go"], "plan": "do the thing", "complexity": "medium"}
</bail-report>`

	report := extractBailReport(input)
	if report == nil {
		t.Fatal("expected bail report")
	}
	if report.Reason != "second attempt" {
		t.Errorf("reason = %q, want 'second attempt' (should use last block)", report.Reason)
	}
}

func TestFormatBailComment(t *testing.T) {
	report := &BailAssessment{
		Reason:        "Needs design decisions",
		FilesExamined: []string{"auth.go", "middleware.go"},
		Plan:          "1. Define flow\n2. Implement",
		SubIssues:     []string{"Sub-issue A", "Sub-issue B"},
		Complexity:    "large",
	}

	comment := formatBailComment(report)

	checks := []struct {
		name    string
		substr  string
		present bool
	}{
		{"header", "## Agent Bail Report", true},
		{"reason", "**Reason:** Needs design decisions", true},
		{"complexity", "**Complexity:** large", true},
		{"files header", "### Files Examined", true},
		{"file entry", "`auth.go`", true},
		{"plan header", "### Implementation Plan", true},
		{"plan content", "1. Define flow", true},
		{"decomp header", "### Suggested Decomposition", true},
		{"sub-issue", "Sub-issue A", true},
		{"footer", "Generated by minder agent", true},
	}

	for _, c := range checks {
		found := strings.Contains(comment, c.substr)
		if found != c.present {
			t.Errorf("%s: contains(%q) = %v, want %v", c.name, c.substr, found, c.present)
		}
	}
}

